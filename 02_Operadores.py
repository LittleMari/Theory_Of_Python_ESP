#Operadores Aritméticas
print(3 + 4) # Suma
print(3 - 4) # Resta
print(3 * 4) # Multiplicación
print(3 / 4) # Divsión
print(3 % 4) # Calcula el resto de una división
print(3 // 4) # División aproximandose a un número entero
print(3 ** 4) # Exponente (3 elevado a 4)

#Operadores Comparativas
print(3 > 4) # Mayor que
print(3 < 4) # Menor que
print(3 >= 4) # Mayor o igual que
print(3 <= 4) # Menor o igual que
print(3 == 4) # Igual que
print(3 != 4) # Distinto de

print("Hola" > "Python") # Cuenta caracteres
print("Hola" < "Python") # Cuenta caracteres

#Operadores Lógicos (Lógica Booleana)
print(3 > 4 and "Hola" > "Python") # False y False = False
print(3 < 4 and "Hola" > "Python") # True y False = False
print(3 < 4 and "Hola" < "Python") # True y True = True
print(3 > 4 or "Hola" > "Python") # False o False = False
print(3 < 4 or "Hola" > "Python") # True o False = True
print(3 < 4 or "Hola" < "Python") # True o True = True
print(not(3 > 4)) # Contrario de False = True
print(not(3 > 4)) # Contrario de True = False